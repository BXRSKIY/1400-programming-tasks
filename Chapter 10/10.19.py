import random

def main():
    # 10.19. Случайная карта одной масти.
    # Достоинства: 6,7,8,9,10, 'валет','дама','король','туз'
    ranks = ["шестерка", "семерка", "восьмерка", "девятка",
             "десятка", "валет", "дама", "король", "туз"]
    r = random.choice(ranks)
    print("Выбрана карта:", r)


if __name__ == "__main__":
    main()
