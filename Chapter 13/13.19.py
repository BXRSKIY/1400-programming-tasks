# 13.19. По каждому дню месяца известны осадки и температура. Найти, сколько осадков выпало как снег и как дождь.
# Считать, что идет дождь, если температура > 0°C, иначе — снег.
# Ввод: n (число дней), затем n строк: "осадки температура" (осадки можно вводить как число).
n = int(input())
snow = 0.0
rain = 0.0
for _ in range(n):
    p, t = map(float, input().split())
    if t > 0:
        rain += p
    else:
        snow += p
# Печатаем два числа
print(snow)
print(rain)
