# 12.27. Из слова "клоун" получить слова "уклон", "кулон" и "колун".
s = "клоун"
# уклон = у(3) к(1) л(2) о(4) н(5)
print(s[2] + s[0] + s[1] + s[3] + s[4])
# кулон = к(1) у(3) л(2) о(4) н(5)
print(s[0] + s[2] + s[1] + s[3] + s[4])
# колун = к(1) о(4) л(2) у(3) н(5)
print(s[0] + s[3] + s[1] + s[2] + s[4])
