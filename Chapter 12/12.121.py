# 12.121. Дано предложение. Удалить из него все буквы 'с'.
# Удаление: каждый раз сдвиг влево + '_' в конец (по правилам 12.116–12.124).
s = input().rstrip("\n")
# Удаляем слева направо: проще собрать без 'с' и добавить '_' столько, сколько удалили.
removed = s.count("с")
t = "".join(ch for ch in s if ch != "с") + "_"*removed
print(t)
