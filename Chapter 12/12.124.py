# 12.124. Дано предложение. Удалить из него все буквы 'о', стоящие на нечетных местах.
# Нечетные места считаются с 1 => индексы 0,2,4,...
# Удаление: сдвиг влево + '_' в конец (по числу удалённых символов).
s = input().rstrip("\n")
out = []
removed = 0
for i, ch in enumerate(s, start=1):
    if ch == "о" and i % 2 == 1:
        removed += 1
    else:
        out.append(ch)
print("".join(out) + "_" * removed)
