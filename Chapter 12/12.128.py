# 12.128. Дано слово, оканчивающееся символом '_'. Вставить букву 'т' после k-й буквы.
# Вставка: сдвиг вправо с (k+1)-й позиции на 1 и запись символа.
s = input().rstrip("\n")
k = int(input())
# предполагаем, что в строке есть '_' в конце и есть место для вставки
lst = list(s)
# сдвиг вправо: идём с конца к (k+1)
for i in range(len(lst)-1, k, -1):
    lst[i] = lst[i-1]
lst[k] = "т"  # (k+1)-я позиция в 1-индексации => индекс k
print("".join(lst))
