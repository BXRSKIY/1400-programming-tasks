# 12.65. Слова разделены одним или несколькими пробелами ('-' нет). Определить количество слов.
# Рассмотреть два случая:
# 1) начальных и конечных пробелов нет;
# 2) начальные и конечные пробелы есть.
s = input().rstrip("\n")
# Универсально: split() по умолчанию сжимает последовательности пробелов и игнорирует края.
words = s.split()
print(len(words))
