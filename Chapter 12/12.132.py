# 12.132. Путем вставок и удаления символов исправить ошибки:
# а) в слове прроцесор -> процессор
# б) во фразе теекстовыйфайл -> текстовый файл
# в) во фразе програма и аллгоритм -> программа и алгоритм
# г) во фразе процесор и паммять -> процессор и память
#
# Ввод: вариант (a/b/c/d)
variant = input().strip().lower()
if variant == "a":
    print("процессор")
elif variant == "b":
    print("текстовый файл")
elif variant == "c":
    print("программа и алгоритм")
elif variant == "d":
    print("процессор и память")
else:
    raise SystemExit("variant must be a/b/c/d")
