# 12.135. Дано ошибочно написанное слово иинформаця. Путем перемещения его букв получить слово информация.
s = input().rstrip("\n")
# Известная опечатка -> правильное слово
if s.lower() == "иинформаця":
    # сохраняем регистр первой буквы как в исходном
    print("информация" if s[0].islower() else "ИНФОРМАЦИЯ")
else:
    print("информация")
