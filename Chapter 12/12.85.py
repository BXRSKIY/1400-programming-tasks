# 12.85. Дан текст. Определить количество букв 'и' в первом предложении.
# Рассмотреть случаи:
# 1) известно, что 'и' в этом предложении есть;
# 2) букв 'и' в тексте может не быть.
#
# Будем считать, что конец первого предложения — первый из символов . ! ?
text = input().rstrip("\n")
end = len(text)
for i, ch in enumerate(text):
    if ch in ".!?":
        end = i
        break
first_sentence = text[:end]
print(first_sentence.count("и"))
