# 12.88. В предложении нет символа '-'. Определить количество букв 'о' в первом слове.
# Учесть, что в начале предложения могут быть пробелы.
s = input().rstrip("\n")
s = s.lstrip(" ")
# первое слово до первого пробела или конца строки
pos = s.find(" ")
first = s if pos == -1 else s[:pos]
print(first.count("о"))
