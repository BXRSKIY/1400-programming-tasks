# 12.133. Дано ошибочно написанное слово рпроцессо. Путем перемещения его букв получить слово процессор.
s = input().rstrip("\n")
# ожидаем "рпроцессо": переносим первую букву в конец и добавляем 'р' (или просто фикс)
if s == "рпроцессо":
    print("процессор")
else:
    # общее: если длина >=1, перенести первый символ в конец
    print(s[1:] + s[:1])
