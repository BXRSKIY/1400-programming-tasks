def main():
    # 11.169. Данные о росте 15 юношей класса, упорядоченные по убыванию,
    # записаны в массиве. В начале учебного года в класс пришёл новый ученик.
    # Составить массив, учитывающий рост нового ученика.
    # Рассмотреть два случая:
    # 1) известен порядковый номер нового ученика;
    # 2) известен рост нового ученика.
    #
    # Формат ввода:
    # 15 ростов (по убыванию),
    # k — позиция нового ученика (1..16),
    # h — рост нового ученика.
    import sys
    data = list(map(float, sys.stdin.read().split()))
    if len(data) < 17:
        return
    heights = data[:15]
    k = int(data[15])
    h = data[16]

    # 1) вставка по номеру (без учёта сортировки)
    arr1 = heights[:]
    if 1 <= k <= len(arr1)+1:
        arr1.insert(k-1, h)
    print("case1:", *arr1)

    # 2) вставка по росту с сохранением порядка убывания
    arr2 = heights[:]
    pos = len(arr2)
    for i, x in enumerate(arr2):
        if h > x:
            pos = i
            break
    arr2.insert(pos, h)
    print("case2:", *arr2)


if __name__ == "__main__":
    main()
