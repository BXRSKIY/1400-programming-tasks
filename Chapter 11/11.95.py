def main():
    # 11.95. В массиве хранятся сведения о среднем количестве осадков за каждый месяц года.
    # Определить суммарное количество осадков за летний период (июнь, июль, август),
    # и за период с ноября по март.
    import sys
    arr = list(map(float, sys.stdin.read().split()))
    if len(arr) < 12:
        arr += [0.0] * (12 - len(arr))
    # месяцы: 1-янв,2-фев,3-мар,4-апр,5-май,6-июн,7-июл,8-авг,9-сен,10-окт,11-ноя,12-дек
    summer = arr[5] + arr[6] + arr[7]  # июн,июл,авг
    # ноябрь, декабрь, январь, февраль, март
    nov_to_mar = arr[10] + arr[11] + arr[0] + arr[1] + arr[2]
    print(summer, nov_to_mar)


if __name__ == "__main__":
    main()
