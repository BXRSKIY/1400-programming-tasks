def main():
    # 11.168. В массиве записана информация о высоте над уровнем моря
    # двадцати горных вершин мира. Данные упорядочены по убыванию.
    # Известна высота новой вершины; массив содержит одно «свободное»
    # место для записи новой информации.
    # Вставить новую вершину так, чтобы упорядоченность по убыванию
    # сохранилась.
    #
    # Формат ввода:
    # h_new
    # затем 20 чисел (включая одно свободное место, которое можно
    # обозначить нулём или отрицательным числом).
    import sys
    data = list(map(float, sys.stdin.read().split()))
    if len(data) < 21:
        return
    h_new = data[0]
    heights = data[1:21]
    # удалим один "свободный" элемент (например, ноль) из конца
    # и вставим новую вершину.
    # Для надёжности просто добавим и затем отсортируем по убыванию.
    heights.append(h_new)
    heights_sorted = sorted(heights, reverse=True)
    # оставим только 20 значений (предполагается, что одно место было свободно)
    res = heights_sorted[:20]
    print(*res)


if __name__ == "__main__":
    main()
