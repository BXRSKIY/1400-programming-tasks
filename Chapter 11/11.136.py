def main():
    # 11.136. «Роза ветров»: массив направлений ветра за каждый день.
    # Кодировки: 1 – северный, 2 – южный, 3 – восточный, 4 – западный,
    # 5 – северо-западный, 6 – северо-восточный,
    # 7 – юго-западный, 8 – юго-восточный.
    # Определить, как должен быть расположен жилой комплекс
    # по отношению к комбинату (нужно минимизировать число дней,
    # когда ветер дует со стороны комбината на жилой комплекс).
    #
    # Т.к. точная формулировка геометрии в тексте повреждена,
    # реализуем подсчёт количества дней по каждому направлению.
    # Интерпретация расположения оставляется пользователю.
    import sys
    arr = list(map(int, sys.stdin.read().split()))
    counts = [0]*9  # индексы 1..8
    for d in arr:
        if 1 <= d <= 8:
            counts[d] += 1
    # выведем частоты по направлениям
    for direction in range(1, 9):
        print(direction, counts[direction])


if __name__ == "__main__":
    main()
