def main():
    # 11.157. В массиве 20 элементов — стоимость каждого из 20 видов товара.
    # С 1 января фирма перестаёт продавать один вид товара, стоимость
    # которого записана в n-м элементе массива.
    # Получить массив со стоимостью оставшихся видов товара
    # с удалением по правилу (смещение и 0 в конец).
    #
    # Формат ввода:
    # n
    # затем 20 стоимостей.
    import sys
    data = list(map(float, sys.stdin.read().split()))
    if len(data) < 21:
        return
    n = int(data[0])
    arr = data[1:21]

    idx = n - 1
    if 0 <= idx < len(arr):
        for i in range(idx, len(arr)-1):
            arr[i] = arr[i+1]
        arr[-1] = 0.0
    print(*arr)


if __name__ == "__main__":
    main()
