def main():
    # 11.49. В массиве хранятся сведения о количестве осадков,
    # выпавших за каждый день февраля.
    # Определить среднедневное количество осадков в этом месяце.
    import sys
    arr = list(map(float, sys.stdin.read().split()))
    # Будем считать, что даны все значения за месяц;
    # среднее по всем введённым элементам.
    if not arr:
        print(0.0)
        return
    avg = sum(arr) / len(arr)
    print(avg)


if __name__ == "__main__":
    main()
