def main():
    # 11.165*. Удалить из массива все повторяющиеся элементы,
    # оставив их первые вхождения (в массиве должны остаться только
    # различные элементы).
    #
    # Будем поддерживать множество уже встреченных значений и
    # формировать новый массив без повторов.
    import sys
    arr = sys.stdin.read().split()
    seen = set()
    res = []
    for x in arr:
        if x not in seen:
            seen.add(x)
            res.append(x)
    print(*res)


if __name__ == "__main__":
    main()
