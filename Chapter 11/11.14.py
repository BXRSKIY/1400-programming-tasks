def main():
    # 11.14. Дано натуральное число n (n ≤ 999999).
    # Заполнить массив его цифрами в обратном порядке.
    # Незаполненные элементы массива равны 0.
    # Будем считать, что массив имеет длину 6.
    num = int(input())
    arr = [0] * 6
    i = 0
    while num > 0 and i < 6:
        arr[i] = num % 10
        num //= 10
        i += 1
    # вывести только элементы, являющиеся цифрами числа n
    # (т.е. до первого нулевого хвоста, если число само не заканчивалось нулями).
    # Но по условию достаточно вывести все ненулевые элементы слева направо.
    digits = [x for x in arr if x != 0]
    print(*digits)


if __name__ == "__main__":
    main()
